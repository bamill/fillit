#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	char	*b;
	size_t	i;

	b = dst;
	i = 0;
	while (b[i] != '\0')
		i++;
	while (*src)
	{
		b[i] = *src++;
		i++;
	}
	b[i] = '\0';
	return (dst);
}
